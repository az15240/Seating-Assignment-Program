    /**
     * Fill all empty characters in front of a given String with '0' given the length of the String result
     * @param inputString the String given
     * @param length the length of desired String result
     * @return the String result with all zeros on the left
     */
    public static String padLeftZeros(String inputString, int length) {
        if (inputString.length() >= length) {
            return inputString;
        }
        StringBuilder sb = new StringBuilder();
        while (sb.length() < length - inputString.length()) {
            sb.append(' ');
        }
        sb.append(inputString);
        return sb.toString();
    }
    
