    private void confirmChooseYourLayoutButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmChooseYourLayoutButtonActionPerformed
        //choose a valid layout
        String yourLayoutChoice = null;
        yourLayoutChoice = yourChoiceChooseYourLayoutTextField.getText();
        
        for(Layout myLayout:layouts) {
            if(myLayout.getName().equals(yourLayoutChoice)) {
                String pathName="Accounts/"+currentAccount.getUsername()+"/"+yourLayoutChoice+".txt";
                try {
                    currentLayout=new Layout(new File(pathName));
                } catch (FileNotFoundException ex) {
                    Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                if(students.size()!=currentLayout.getNumberOfSeats()) {
                    JOptionPane.showMessageDialog(chooseYourLayoutFrame, "Student number = "+students.size() + ".\nNumber of seats = "+ currentLayout.getNumberOfSeats() + ".\nNumber of student does not match!");
                    yourChoiceChooseYourLayoutTextField.setText("");
                    return;
                }
                else {
                    JOptionPane.showMessageDialog(chooseYourLayoutFrame, "Success. Your layout is "+yourLayoutChoice+".");

                    clearTextComponents(startAssigningSeatsFrame);
                    startAssigningSeatsFrame.setVisible(true);
                    startAssigningSeatsFrame.pack();
                    
                    //create a new scheme                    
                    currentScheme = new Scheme(currentLayout,students);
                    currentScheme.RandomSeating();
                    startAssigningSeatsTextArea.setText(currentScheme.getOutputText());
                    
                    return;
                }
            }
        }
        JOptionPane.showMessageDialog(chooseYourLayoutFrame, "This name is not found! Input again!");
        yourChoiceChooseYourLayoutTextField.setText("");
    }//GEN-LAST:event_confirmChooseYourLayoutButtonActionPerformed

