    /**
     * Clear all text components in jf
     * @param jf a JFrame to be cleared
     */
    private void clearTextComponents(JFrame jf) {
        for (Component c : jf.getContentPane().getComponents()) {
            if (c instanceof JTextField) {
                ((JTextComponent) c).setText("");
            }
        }
        for (Component c : jf.getComponents()) {
            if (c instanceof JTextArea) {
                ((JTextArea) c).setText("");
            }
        }
    }
    
