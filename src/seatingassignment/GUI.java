    private void nextInputCoordinateCanceledButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextInputCoordinateCanceledButtonActionPerformed
        int _x,_y;
        try {
            _x=Integer.parseInt(rowNumberInputCoordinateCanceledTextField.getText());
            _y=Integer.parseInt(columnNumberInputCoordinateCanceledTextField.getText());    
        } catch(NumberFormatException e) {
            JOptionPane.showMessageDialog(inputCoordinateCanceledFrame,"Invalid number format!");
            rowNumberInputCoordinateCanceledTextField.setText("");
            columnNumberInputCoordinateCanceledTextField.setText("");
            return;
        }
        
        if(_x<1 || _x>currentLayout.getRows() || _y<1 || _y>currentLayout.getColumns()) {
            JOptionPane.showMessageDialog(inputCoordinateCanceledFrame,"Invalid range!");
            rowNumberInputCoordinateCanceledTextField.setText("");
            columnNumberInputCoordinateCanceledTextField.setText("");
            return;
        }
        
        for(Pair _pr:currentLayout.getPositionCanceled()) {
            if(_pr.equals(new Pair(_x,_y))) {
                JOptionPane.showMessageDialog(inputCoordinateCanceledFrame,"Already exist!");
                rowNumberInputCoordinateCanceledTextField.setText("");
                columnNumberInputCoordinateCanceledTextField.setText("");
                return;
            }
        }
        
        currentLayout.addPositionCanceled(_x,_y);
        currentLayout.setMapAtPosition(_x,_y);
        currentLayout.incrementCounter();
        
        String temp=""+currentLayout.getCounter();
        if(currentLayout.getCounter()%10==1) temp+="st";
        else if(currentLayout.getCounter()%10==2) temp+="nd";
        else if(currentLayout.getCounter()%10==3) temp+="rd";
        else temp+="th";
        
        inputCoordinateCanceledLabel.setText("Input "+temp+" coordinate canceled");
        rowNumberInputCoordinateCanceledTextField.setText("");
        columnNumberInputCoordinateCanceledTextField.setText("");
        
        if(currentLayout.getCounter()==currentLayout.getNumberCanceled()+1) {
            currentLayout.setCounter(0);
            JOptionPane.showMessageDialog(inputCoordinateCanceledFrame,"Finished!");
            inputCoordinateCanceledFrame.dispose();
            
            clearTextComponents(layoutDisplayFrame);
            layoutDisplayFrame.setVisible(true);
            layoutDisplayFrame.pack();
            
            String layoutDisplayTextAreaText=currentLayout.getMapInString();
            layoutDisplayTextArea.setText(layoutDisplayTextAreaText);
            
            return;
        }
    }//GEN-LAST:event_nextInputCoordinateCanceledButtonActionPerformed

